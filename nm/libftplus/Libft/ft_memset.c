#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ptr2;
	size_t	i;

	ptr2 = b;
	i = 0;
	while (i < len)
		ptr2[i++] = c;
	return (ptr2);
}
